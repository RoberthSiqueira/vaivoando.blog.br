<img class="img-responsive" src="img/bagagemmao.jpg" alt="" style="margin-top:30px;">
<p>Entre as dúvidas que aparecem na hora de fazer as malas para viajar de avião se

refere ao que se pode ou não levar na bagagem. Afinal, uma das coisas mais

desagradáveis ao chegar no aeroporto com as malas prontas para fazer o check-in e

despachá-las, é lembrar que em sua bagagem de mão tem algum objeto proibido.

Para evitar que isso aconteça veja algumas regras básicas para embarcar sem

sustos.</p>
<p><b>Tamanho da mala</b></p>
  <p>Um detalhe muito importante sobre bagagem de mão é o tamanho que a ela deve

ter. Em voos domésticos, a bagagem não pode ser maior que 115 cm (considerando

altura + comprimento + largura) e o peso máximo é de 5 kg.</p>

<p><b>O que <u>não</u> pode ser levado na bagagem de mão</b></p>
  <p>Ferramentas e produtos químicos: A maioria das ferramentas como alicates e

martelos estão proibidos na bolsa de mão. Da mesma forma, produtos químicos

como sprays, cloro, acetona e vários outros também são proibidos.

Objetos cortantes e armas de fogo: Por motivo de segurança não podem ser

levados arma (ou réplica) de qualquer tipo; objetos pontiagudos, cortantes ou de

ponta arredondada que possam ser usados para causar ferimentos (alicates de

unha, por exemplo); substâncias explosivas ou inﬂamáveis, químicas ou tóxicas que

possam colocar em risco a integridade física de pessoas ou a segurança do avião.</p>

<p><b>O que <u>pode</u> ser levado como bagagem de mão</b></p>
<p>Segundo a Anac, você pode levar:</p>

<ul>
  <li>Uma bolsa de mão, maleta ou equipamento que possa ser acomodado embaixo do assento do passageiro ou no compartimento próprio da aeronave na cabine;</li>
  <li>Um casaco, sobretudo, manta ou cobertor;</li>
  <li>Um guarda-chuva ou bengala (não pontiagudos);</li>
  <li>Máquina fotográfica, laptop ou binóculos;</li>
  <li>Material de leitura para viagem;</li>
  <li>Alimentação infantil para consumo durante a viagem;</li>
  <li>Muletas ou aparelhos ortopédicos utilizados pelo passageiro.</li>
  <li>Instrumentos musicais (desde que os mesmos possam passar pelos equipamentos de Raio – X nos postos de segurança);</li>
  <li>Um carrinho de bebê dobrável, uma cesta ou um bebê conforto.</li>
</ul>

<p><b>Sempre gera dúvidas, mas <u>é</u> <u>permitido</u></b></p>

<p>Líquidos: há alguns anos foi proibido transportar mais de líquidos com mais de

100ml em voos internacionais, o que acabou gerando confusão para os passageiros.

No entanto, em voos nacionais, líquidos como shampoo, condicionador, creme e

desodorante, entre outros, são permitidos na mala de mão.</p>

<p>Aparelhos eletrônicos: eles não só podem como devem ser levados na bagagem de

mão, para evitar o risco de quebrarem. Além disso, eles ajudam a passar o tempo

quando a viagem é longa e o avião não tem entretenimento.</p>
