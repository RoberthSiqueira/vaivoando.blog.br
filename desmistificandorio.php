Os arcos da lapa são um dos cartões postais do Rio de Janeiro

<p>Um dos destinos mais famosos do mundo, o Rio de Janeiro, está no centro das

atenções de todo o mundo, uma vez que irá sediar os Jogos Olímpicos. Famosa

pelas praias, a cidade oferece muitas outras atrações que agradam das crianças aos

idosos. Neste textos, nós vamos mostrar que, além de um lugar com uma linda orla,

o Rio tem algo muito mais esplêndido: as pessoas, a cultura e uma agitada vida

noturna.</p>

<p><b>Pão de açúcar: começando o dia bem</b></p>
  <p>Comece cedo o seu dia indo ao Pão de Açúcar logo pela manhã. Almoce nos

restaurantes da Urca e a tarde vá ao Cristo Redentor (tente ver o pôr-do- sol lá de

cima. É incrível!). Uma dica que economiza muito tempo é comprar ingresso

antecipado para evitar filas e poupar tempo.

Após o passeio, com direito a um pôr-do- sol ao lado de uma das 7 maravilhas do

mundo, que tal curtir um local boêmio e com muita música e cultura? Vamos

conhecer a Lapa.</p>

<p>P.S.: existem algumas mochilas que são dobráveis e cabem no bolso. São super práticas para

você levar para a praia, fazer uma pequena trilha ou passear pela cidade. É um bom

investimento para quem gosta de viajar.</p>

<p><b>Lapa</b></p>
  <p>A Lapa é, historicamente, o lugar dos animados boêmios cariocas, cheia de casas

de samba e bares que são famosos em todo o mundo. Mais que um bairro musical,

a Lapa é hoje um polo de cultura. Repleta de museus, teatros e edifícios históricos

que criam uma atmosfera própria para o lugar.

E claro que no lugar onde há cultura, história e muitas festa não poderia faltar ótimos

restaurantes. Feijoada, pizzas, massas e rodízio de comida japonesa são algumas

das opções que não só saciam a fome, como encantam até mesmo os amantes da

boa cozinha.

A Lapa também possui diversos entretenimentos que agradam todos os gostos e

bolsos. O Circo Voador, por exemplo, promove todas as semanas shows de grandes

bandas nacionais e internacionais. Logo ao lado, sob os arcos da Lapa, baterias de

samba se reúnem, para batucar junto a um dos mais famosos monumentos da

cidade. Cerveja e churrasco são vendidos em barraquinhas. Outra opção, para

quem gosta de programas mais refinados, é o Rio Scenarium. O casarão promove,

principalmente aos fins de semana, ótimos shows em seus múltiplos ambientes.</p>

<p><b>Escadaria Selarón</b></p>
  <p>Que tal, após passar na Lapa, visitar um point turístico do grande artista chileno

Jorge Selarón? A chamada Escadaria Selarón começou a ser renovada em 1990

pelo artista, que se incomodou com o estado da escada em frente a sua casa. Ela

se tornou um sucesso após anos de dedicação do artista, tornando-se em um belo

cartão postal da cidade. A escada liga os bairros da Lapa e Santa Tereza.</p>

<p><b>Floresta da Tijuca</b></p>
  <p>Se você curte fazer trilhas e explorar paisagens deslumbrantes, você vai se

maravilhar com essa dica. Tire um dia para conhecer a Floresta da Tijuca, a maior

floresta urbana do mundo. No local dá para curtir algumas trilhas curtas e tomar uns

banhos de cachoeiras. A Vista Chinesa é bem perto e também vale uma passada

rápida para umas fotos. Tire a noite para curtir um agito no Baixo Gávea. Uma

região bem divertida, com ótimos bares repleto de pessoas mais jovens.</p>

