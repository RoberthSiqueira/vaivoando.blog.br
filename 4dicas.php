3. Use milhas
<p>Viajar de ônibus ou carro demora muito e você acha que pegar carona é perigoso?</p>
<p>Se viajar de avião é a única solução, uma dica é recorrer a amigos e familiares que têm

milhas acumuladas prestes a expirar e comprá-las. <b>Uma alternativa ainda melhor é</b>

utilizar sites de milhagem que fazem a intermediação entre quem possui milhas

acumuladas e quem está procurando uma passagem de avião mais barata. Os valores

das passagens aéreas nesses sites podem chegar a ser <b>até 80% mais baratas</b>.</p>
<p>O site <a href="http://www.maxmilhas.com.br" target="_blank">Max Milhas</a>, por exemplo, é uma boa opção para comprar passagens mais baratas.</p>
