<p>2. Use o Couchsurfing</p>
<p>Couchsurfing é vida se você quer uma hospedagem de graça e de quebra fazer amigos

e conhecer os hábitos e a cultura de pessoas locais. A plataforma te conecta com

pessoas de todo o mundo dispostas a receberem estrangeiros e fazerem amigos. Uma

dica é criar o seu perfil na plataforma (<a href="https://www.couchsurfing.com" target="_blank">https://www.couchsurfing.com</a>), adicionar seus

amigos e pedir que eles e pessoas que já te hospedaram ou as que você hospedou te

referenciarem. Isso facilita muito na hora de conseguir um host, afinal todo mundo

quer saber antes sobre a experiência de ter você como hóspede. Outra dica para

quando entrar em contato com possíveis hosts é ler o perfil da pessoa antes de

qualquer coisa e sempre enviar mensagens personalizadas para aqueles que te

interessarem. Nada de Ctrl C + Ctrl V.</p>
</li>

