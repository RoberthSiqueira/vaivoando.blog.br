                    <p>Manaus, São Paulo, Salvador, Belo Horizonte, Brasília e Rio de Janeiro serão as cidades-sede das Olimpíadas Rio 2016. Além de assistir aos jogos olímpicos, veja sugestões de passeios.</p>
                    
<p><b>Manaus:</b> A capital do Amazonas tem uma variedade de paisagens. Destino preferido dos amantes da natureza, apreciar o encontro dos rios Negro e Solimões é um espetáculo que pode ser observado todos os dias. Em terra, O Teatro Amazonas, símbolo do ciclo da Borracha encanta com sua bela fachada neoclássica pintada em rosa. O Largo de são Sebastião é outro lugar para passear. Fica nos arredores do Teatro Amazonas e reúne grandes monumentos da história manauara, como a Igreja de São Sebastião, o Palácio da Justiça e o consagrado Teatro Amazonas. Não deixe de saborear as receitas típicas preparadas com tucupi, gengibre e cupuaçu e os peixes típicos como o tucunaré, o pirarucu e o tambaqui.
<p><b>Salvador:</b> Conheça o Centro Histórico, passando pelo Pelourinho, Elevador Lacerda e Cidade Baixa. Lá você irá apreciar a Igreja e Convento de São Francisco, Catedral Basílica, Solar do Ferrão, Palácio Rio Branco, Igreja Nossa Senhora da Conceição da Praia e Mercado Modelo. Na rota cultural há o Museu de Arte da Bahia, Carlos Costa Pinto e Palacete das Artes Rodin Bahia. O Farol da Barra é a grande estrela da cidade e dá nome a uma das melhores praias soteropolitanas. Se quiser aproveitar o sol, Porto, Buracão, Itapuã e Stella Maris são excelentes destinos no fim de semana.  
<p><b>Brasília:</b> O eixo monumental, formado pelo Congresso Nacional, Catedral, Palácio do Planalto, Museu da República e Memorial JK é um passeio que deve ser cumprido por todo turista, não se esquecendo do mirante da Torre de TV. O Pontão do Lago Sul, na margem do Paranoá, é excelente para caminhar pelo calçadão vendo os barcos e veleiros. O lugar é concorrido ao entardecer e cheio de bares e restaurantes.  
<p><b>Rio de Janeiro:</b> Além do Cristo Redentor e o Pão de Açúcar, vale a pena visitar a Lapa, berço da boemia, e também seus Arcos, uma construção da época colonial que marca a paisagem local. A escadaria do Convento de Santa Teresa, mais conhecida como Escadaria Selarón, também é uma das atrações mais conhecidas. Copacabana é uma das praias mais famosas do mundo, movimentada durante todo o dia. De lá também é possível ir a Ipanema, Leme, Botafogo. O Centro do Rio abriga muitas igrejas da época da Colônia e do Império. Alguns pontos do trajeto são próximos tornando-se possível passear por todos em torno de 4 a 5 horas.
<p><b>Belo Horizonte:</b> O entorno da lagoa da Pampulha reúne o mais belo conjunto arquitetônico da cidade, com três obras assinadas por Niemeyer. O Mercado Central reúne mais de 400 lojas onde se encontra de quase tudo. Cachaças, queijos, cerveja gelada e bons tira-gostos são predominantes. Domingo é dia de ir à feira da Afonso Pena. São milhares de barracas onde se encontram objetos de decoração, artes plásticas, bijuterias, artesanato infantil, móveis, tapeçaria, vestuário e comidas típicas.
<p><b>São Paulo:</b> Há muito que fazer na maior metrópole brasileira. O MASP - Museu de Arte de São Paulo abriga coleções de arte européia e brasileira. Seguindo a linha cultural, o Museu do Ipiranga possui um acervo valioso com peças da República e do Império, além de uma biblioteca com mais de 100 mil volumes. O parque do Ibirapuera é um lugar de descanso e lazer. É possível alugar bicicletas e caminhar pela enorme área verde. A fauna e a flora garantem o cenário de tranquilidade. Para o entretenimento, não deixe de ir a avenida Paulista, rua Augusta  e a Vila Madalena, reduto de bares e boa comida. 
