<img class="img-responsive" src="img/viagemnegocios.jpg" alt="" style="margin-top:30px;">
<p>Quem disse que viagens a trabalho não podem ser tão divertidas e prazerosas

quanto viagens de férias? E, para te ajudar a garantir bons resultados e momentos

de lazer, selecionamos algumas dicas de como aproveitar bem seu tempo e evitar

problemas no trabalho.</p>

<b>1.  Documentos à mão</b>
  <p>Confira e coloque todos os documentos necessários na mala separe o material de

trabalho que você vai precisar durante a viagem, como contratos, planilhas, cartões,

folders e brindes. Organize todos esses papeis dentro uma pasta e coloque na mala.</p>

<p><b>2. Montando a mala certa</b></p>
<p>Escolher roupas neutras e sóbrias que montam o maior número de looks possível.

Quanto menos peças e itens você levar, mais fácil será para se organizar na hora de

voltar para casa.</p>

<p><b>3. Organize suas atividades obrigatórias</b></p>
<p>Programe-se para todos os seus compromissos, levando em consideração o tempo

de deslocamento de um lugar para o outro. Saia sempre com antecedência, pois

nunca se sabe os imprevistos que podem acontecer no caminho.</p>

<p><b>4. O que pode gastar?</b></p>
<p>Leve em consideração a política de gastos de viagem da sua empresa. Você tem

limite para almoços? Pode consumir itens do frigobar do hotel? Saber essas

informações é muito importante para que você não tenha que arcar com despesas

posteriormente.</p>

<p><b>5. Controle seus gastos</b></p>
<p>Peça comprovante de cada gasto que viver, como hotel, transporte e refeição.

Controlar seus gastos garante que você seja devidamente reembolsado e mostra

que você é responsável e organizado. Se alguém no futuro perguntar o motivo de

algum gasto, você saberá como responder!</p>

<p><b>6. Não abuse do seu cartão corporativo</b></p>
<p>Caso você tenha cartão coorporativo, não misture seus gastos profissionais com os

pessoais. Ao evitar gastos desnecessários no cartão da empresa, você mostra

seriedade e comprometimento e profissionalismo. Além de ficar bem com seu chefe,

não será cobrado por gastos extras.</p>

<p><b>7. Você não está de férias</b></p>
<p>Apesar de ser uma viagem, você não está de férias. Seja profissional e mostre que

você realmente foi a trabalho. É preciso que você esteja sempre disponível para a

empresa, por telefone ou e-mail.</p>

<p><b>8. Tenha um contato de segurança</b></p>
<p>É importante sempre ter um contato em casos de imprevistos que vai te ajudar a

resolver os problemas.</p>

<p><b>9. Smartphone: o grande aliado</b></p>
<p>Os smartphones permitem que nos controlemos e planejemos melhor. Fazer o seu

check-in, por exemplo, evita chá de cadeira, atrasos e outros problemas no

aeroporto. Afinal, você não que perder o voo, né?</p>

<p><b>10. Aproveite o tempo de espera</b></p>
<p>Chegou no aeroporto mais cedo? Aproveite para checar os e-mails, fazer ata de

reunião ou mesmo produzir um relatório. Assim, sua rotina estará mais organizada

ao voltar para o escritório.</p>

