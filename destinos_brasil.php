<img class="img-responsive" src="img/maragogi.jpg" alt="" style="margin-top:30px;">Imagem: Maragogi é um pedacinho do paraíso em Alagoas
<p>Desconectar-se da rotina diária e ter um tempo off  é essencial para descansar e renovar as energias. Viajar é um excelente investimento que dificilmente se concretiza, pois muitas vezes é deixado para o segundo plano.
<p>Viajar aumenta o nosso conhecimento, possibilita conhecer novas culturas e cria memórias para toda vida. Pensando em um destino para as próximas férias, separamos quatro lugares espetaculares no Brasil para você conhecer, se divertir e descansar, confira:  
<p><b>Natal:</b> A capital do Rio Grande do Norte é famosa pelas praias e dunas. São muitas opções de passeio, dos mais tranquilos aos agitados. O Parque das Dunas tem três famosas trilhas (Perobinha, Peroba e Ubaia Doce), com duração de 40 minutos a duas horas e meia. Ponta Negra é a praia mais badalada, ponto de encontro de banhistas, surfistas, adeptos do windsurf e do kitesurf. O Morro do Careca é sua marca registrada: uma duna de 120 metros de altura e cercada por vegetação.
<p><b>Maragogi:</b> Conhecida como o Caribe brasileiro as praias paradisíacas são cercadas por coqueiros, águas límpidas e esverdeadas, fazendo jus à comparação. Maragogi é a segunda cidade mais visitada do estado de Alagoas. Os mergulhos permitem observar os arrecifes de corais e ficar rodeado de peixes coloridos. As piscinas naturais ficam a cerca de 6km das praias e é  preciso programar o passeio para dias de maré baixa. Não deixe de conhecer Burgalhau, Barra Grande e Peroba, outras praias de parada obrigatória. 
<p><b>Campina Grande:</b> A cidade é conhecida por seu maior evento: a festa de São João que acontece em junho no Parque do Povo, área que concentra 300 barracas com bares e restaurantes. Embarque no Trem do Forró com destino a Galante para desfrutar dois oito vagões embalados pelos sons de triângulo, sanfona e zabumba durante uma hora e meia de viagem.  Campina Grande é a terra da carne de sol, iguaria servida com manteiga de garrafa, pirão de leite, feijão verde, farofa d’água e mandioca.   
<p><b>Morro de São Paulo:</b> O local é um destino bastante cobiçado e fica a 248 km de Salvador. São quatro praias – identificadas por ordem numérica - e duas ruas principais. As praias de águas cristalinas estão cercadas por um grande coqueiral e barreiras de recifes, formando piscinas naturais quando a maré está baixa. A Feirinha de Artesanato acontece todas as noites na Praça Aureliano Lima. São vendidos objetos de decoração, vestuário, bijuterias, artigos de prata e outros itens. 
<p>Chapada dos Guimarães:</b> O parque nacional fica em Mato Grosso. O acesso é fácil, são 70 km a partir do aeroporto de Cuiabá. Os paredões se espalham por mais de 150 km e tem uma beleza impressionante. As principais atrações são cavernas, lagoas, cachoeiras, paredões e trilhas em uma vegetação típica do Cerrado. Embora seja uma cidade pequena, há boa variedade gastronômica, de comidinhas de rua aos picolés com sabores do Cerrado, e um restaurante praticamente pendurado em um dos paredões de onde é possível contemplar uma bela vista. 
