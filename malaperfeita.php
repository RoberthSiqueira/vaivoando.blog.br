<p>Na hora de viajar é sempre a mesma história, a empolgação é tão grande quanto os

preparativos e fazer às malas acaba ficando para a última hora. Para algumas pessoas indecisas

ou extremamente organizadas esse processo pode virar uma pequena sessão de tortura.</p>

<p>Se você é uma dessas pessoas, fique atento às dicas abaixo para acabar com seus problemas na hora de arrumar a mala.</p>

<p><b>1. Pesquise sobre o local</b></p>
<p>Dica básica. <b>Pesquise sobre o seu destino</b>, especialmente se você estiver indo fazer

uma viagem de longo prazo. É importante saber se lá faz frio ou calor, se chove muito,

se as temperaturas variam com muita facilidade. Se for para outro país pesquise sobre

a cultura e sobre vestimentas adequadas, alguns países têm regras que devem ser

respeitadas.</p>
<p>Além disso, <b>planeje arrumar as malas com uma antecedência de 2 ou 3 dias</b>, assim

fica mais fácil saber se você está realmente levando o que é necessário ou precisa tirar

algumas coisas para não correr o risco de carregar uma mala muito pesada ou pagar

por excesso de bagagem.</p>

<p><b>2. Faça um checklist</b></p>
<p>Essencial para não esquecer de nenhum item. Enquanto você vai pondo roupas e

outros objetos dentro da mala é só ir riscando do checklist. Sensação de estar

esquecendo alguma coisa nunca mais!</p>
<img class="img-responsive" src="img/checklist.jpg" alt="" style="margin-top:30px;">

<p><b>3. Aproveite o espaço da melhor forma</b></p>
<ul>
  <li>Coloque meias e cintos dentro dos sapatos para economizar espaço;</li>
  <li>Faça rolos com camisetas, assim elas ocupam menos espaço e se encaixam

melhor;</li>
  <li>Coloque jeans e roupas que amarrotam menos por baixo;</li>
  <li>Inverta o sentido de calças, bermudas e camisas para construir camadas

homogêneas;</li>
  <li>Coloque objetos avulsos como cabos, carregadores, fones de ouvido em um

estojo;</li>
  <li>Invista em frascos pequenos para produtos de higiene pessoal;</li>
  <li>Prefira levar roupas básicas, assim você garante que tenha peças para usar em

quase todas as situações.</li>
</ul>

<img class="img-responsive" src="img/mala.jpg" alt="" style="margin-top:30px;">

<p><b>Para mochileiros</b></p>
<ul>
  <li>Leve somente o essencial para não carregar muito peso nas costas;</li>
  <li>Tenha etiquetas na mochila com seu nome e contato;</li>
  <li>Dobre as roupas na mesma largura e comprimento da mochila;</li>
  <li>O peso deve estar concentrado na cintura e não nos ombros, por isso coloque as roupas mais pesadas primeiro;</li>
  <li>Deixe as roupas que você irá usar diariamente e uma toalha por cima ou em um compartimento diferente e de fácil acesso;</li>
</ul>

