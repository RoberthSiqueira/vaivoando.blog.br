<p>Seja em voos longos ou curtos, para destinos nacionais ou internacionais, ficar sentado em um avião, por mais rápida que seja a viagem, é um desconforto para todo viajante. Mas com objetos e distrações certas a viagem pode se tornar até mais agradável e produtiva.</p>
<ul>
<li>1.  Tenha em mãos alguma distração
Escolha de acordo com aquilo que gosta de fazer, pode ser um livro ou uma revista, um iPod para ouvir suas músicas preferidas ou aplicativos de jogos. Dessa forma você garante um entretenimento por boa parte da viagem e esquece dos desconfortos de voar. Você também pode bater um bom papo se estiver viajando acompanhado!
</li>
<p>
<li>
2.  Assista um filme ou série
Uma boa hora para você ficar em dia com sua lista de filmes a assistir ou suas séries favoritas. Se estiver em um voo internacional você pode fazer isso no televisor instalado na poltrona a sua frente. As companhias aéreas costumam disponibilizar uma boa programação de filmes e séries. Se preferir, antes do voo, você pode criar sua própria programação, armazenar em algum dispositivo eletrônico que tiver e assistir durante a viagem. 
<img class="img-responsive" src="img/aviao_1.jpg" alt="" style="margin-top:30px;">
</li>
<p>
  <li>
3.  Seja produtivo no trabalho
Se você está viajando para ir àquela reunião importante ou está sempre atolado de trabalho, esse é um bom momento para ser produtivo. Você pode montar ou revisar apresentações e planilhas, deixar escritos os e-mails que precisam ser enviados, ler relatórios ou estudar.
<img class="img-responsive" src="img/aviao_5.jpg" alt="" style="margin-top:30px;">
</li>
<p>
<li>
4.  Planeje a viagem
Por que não planejar a viagem durante o voo? Faça um checklist de coisas que você quer fazer ou lugares que gostaria de conhecer e programe o que gostaria de fazer durante cada dia. Assim você garante que vai aproveitar ao máximo cada dia.
</li>
<p>
<li>
5.  Fique confortável
Prefira usar roupas leves e confortáveis. Tenha uma meia para tirar os sapatos e não sentir frio no pé, um casaco pela mesma razão, um travesseiro de viagem, máscara para os olhos, tapa ouvidos, produtos de higiene pessoal e remédios que costuma ou necessita tomar. Assim, dormir vai ficar mais fácil e confortável.
<img class="img-responsive" src="img/aviao_2.jpg" alt="" style="margin-top:30px;">
</li>
<p>
<li>
6.  Prefira comidas mais leves
As comidas servidas durante o voo variam de acordo com a companhia aérea e nem sempre são saudáveis. Na hora de escolher entre as opções, prefira alimentos mais leves para evitar enjoos e outros desconfortos. Se preferir ou possuir restrições alimentares leve o seu próprio lanche.
<img class="img-responsive" src="img/aviao_3.jpg" alt="" style="margin-top:30px;">
</li>
